                    </a>
                </p>
            </div>
            <div class="col-md-8 offset-md-1">
                <div class="collapse mb-3" id="collapse03">
                    <div class="card card-body shadow h5 p-3">
                        可能原因包括以下幾點：<br>
                            靜電積聚：金屬粉末導電性高，靜電積聚在噴槍或工件上，容易導致漏電。<br>
                            接地不良：設備或工件接地不良，無法有效釋放靜電。<br>
                            環境濕度：濕度過高或過低都會影響靜電釋放，增加漏電風險。<br>
                            噴槍故障：靜電噴槍的電極損壞或絕緣材料老化，可能導致漏電。<br>
                            粉末質量問題：金屬粉末含水量高或顆粒不均，也可能導致靜電問題。
